require("dotenv").config();
const express = require("express");
const axios = require("axios");
const app = express();

app.set("view engine", "pug");
app.use(express.static(__dirname + "/public"));
app.use(express.urlencoded({ extended: true }));
app.use(express.json());

const PRIVATE_APP_ACCESS = process.env.PRIVATE_APP_ACCESS;

// Example: pets
const CUSTOM_OBJECT = "pets";

// Common headers
const headers = {
  Authorization: `Bearer ${PRIVATE_APP_ACCESS}`,
  "Content-Type": "application/json",
};

/**
 * ROUTE 1
 * Homepage â€“ display custom object records in a table
 */
app.get("/", async (req, res) => {
  const endpoint = `https://api.hubspot.com/crm/v3/objects/${CUSTOM_OBJECT}?properties=name,description,age`;

  try {
    const response = await axios.get(endpoint, { headers });
    const records = response.data.results;

    res.render("homepage", {
      title: "Custom Objects | Integrating With HubSpot I Practicum",
      records,
    });
  } catch (error) {
    console.error(error.response?.data || error.message);
    res.send("Error fetching custom object data");
  }
});

/**
 * ROUTE 2
 * Render form to create a new custom object record
 */
app.get("/update-cobj", (req, res) => {
  res.render("updates", {
    title: "Update Custom Object Form | Integrating With HubSpot I Practicum",
  });
});

/**
 * ROUTE 3
 * Handle form submission and create a new custom object record
 */
app.post("/update-cobj", async (req, res) => {
  const newRecord = {
    properties: {
      name: req.body.name,
      description: req.body.description,
      age: req.body.age,
    },
  };

  const endpoint = `https://api.hubspot.com/crm/v3/objects/${CUSTOM_OBJECT}`;

  try {
    await axios.post(endpoint, newRecord, { headers });
    res.redirect("/");
  } catch (error) {
    console.error(error.response?.data || error.message);
    res.send("Error creating custom object record");
  }
});

// ðŸš€ Server
app.listen(3000, () => console.log("Listening on http://localhost:3000"));
